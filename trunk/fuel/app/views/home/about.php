                    <p>Dans le contexte actuel de transition énergétique, l’énergie éolienne tient une place importante et a un rôle à jouer. Dans cette optique, l’ECE Paris, école d’ingénieur généraliste, forme ses étudiants pour qu’ils puissent répondre à ces nouvelles problématiques. Une part du programme du cours d’énergies renouvelables de ces étudiants est consacrée à l’énergie éolienne. </p><br>
